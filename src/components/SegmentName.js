export function getFullName(label){
    if (label === 'rep'){ return 'Republican' }
    else if (label === 'dem') { return 'Democrat' }
    else if (label === 'other_party') { return 'Other Party'}
    else if (label === 'male') { return 'Male'}
    else if (label === 'female') { return 'Female'}
    else if (label === 'unknown_sex') { return 'Unknown Sex'}
    else if (label === 'black') { return 'Black'}
    else if (label === 'hispanic') { return 'Hispanic'}
    else if (label === 'white') { return 'White'}
    else if (label === 'other_race') { return 'Other Race'}
  }
  